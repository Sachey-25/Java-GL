package Javaloops;

public class whileloop {

	public static void main(String[] args) {
		// While -- Condition true === loop-body!!
		int i=1;
		while(i<=10) {
			System.out.println("While loop: "+ i);
			i++;
		}
		System.out.println("---------------------------------");
		int j=1;
		while(j<=5) {
			System.out.println("While loop: "+ j);
			j++;
		}
		System.out.println("---------------------------------");
		int k=1;
		while(k<=6) {
			System.out.println("While loop: "+ k);
			k++;
		}
		System.out.println("---------------------------------");
		int l=1;
		while(l<=7) {
			System.out.println("While loop: "+ l);
			l++;
		}
		System.out.println("---------------------------------");
		int m=1;
		while(m<=4) {
			System.out.println("While loop: "+ m);
			m++;
		}
		
	}
}